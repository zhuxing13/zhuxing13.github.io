// 获取 select 元素和 image 元素
const selectElement = document.getElementById('charSelect');
const imageElement = document.querySelector('#imageContainer img');

selectElement.addEventListener('change', function() {
    console.log(selectElement, selectElement.value,characterAttributes[selectElement.value])
    const selectedValue = selectElement.value;
    const imageUrl = 'https://wiki.biligame.com/gt/Special:Redirect/file/' + characterAttributes[selectElement.value].map + '头像立绘.png';
    const backupImageUrl = 'image/character/' + selectedValue + '.png';

    const image = new Image();

    image.onerror = function() {
      // 图片加载失败，切换到备用图片路径
      imageElement.src = backupImageUrl;
    };
  
    image.onload = function() {
      // 图片加载成功，使用原始图片路径
      imageElement.src = imageUrl;
    };
  
    // 加载图片
    image.src = imageUrl;
    // 当备用图片加载失败时，切换到其他备用图片路径
    imageElement.onerror = function() {
        imageElement.src = 'image/character/选择角色.png';
    };
});